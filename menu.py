    def toggle_controller(self):
        if self.controller_process is None:
            self.start_controller()
        else:
            self.stop_controller()
            
    def start_controller(self):
        if not os.path.exists('controller.py'):
            self.show_error_message("‚ùå B≈ÇƒÖd", "Nie znaleziono pliku controller.py")
            return
            
        try:
            # Ustaw katalog roboczy na katalog skryptu
            script_dir = os.path.dirname(os.path.abspath(__file__))
            controller_path = os.path.join(script_dir, 'controller.py')
            if not os.path.exists(controller_path):
                self.show_error_message("‚ùå B≈ÇƒÖd", f"Nie znaleziono pliku:\n{controller_path}")
                return
                
            # Uruchom kontroler BEZ pokazywania okna konsoli
            self.controller_process = subprocess.Popen([
                sys.executable, 
                controller_path
            ], 
            cwd=script_dir,  # Ustaw katalog roboczy
            creationflags=subprocess.CREATE_NO_WINDOW)  # UKRYJ okno konsoli
            
            self.controller_btn.setText('‚èπÔ∏è Zatrzymaj kontroler')
            self.controller_btn.setStyleSheet(self.controller_button_style_running)
            self.controller_status.setText('‚ñ∂Ô∏è Kontroler uruchomiony')
            self.status_label.setText("‚úÖ Kontroler dzia≈Ça")
            print("‚úÖ Kontroler uruchomiony w tle")
        except Exception as e:
            self.show_error_message("‚ùå B≈ÇƒÖd", f"Nie mo≈ºna uruchomiƒá kontrolera:\n{str(e)}")
            self.controller_process = None
            
    def stop_controller(self):
        if self.controller_process:
            try:
                self.controller_process.terminate()
                self.controller_process.wait(timeout=3)
            except:
                try:
                    self.controller_process.kill()
                except:
                    pass
            self.controller_process = None
            self.controller_btn.setText('‚ñ∂Ô∏è Uruchom kontroler')
            self.controller_btn.setStyleSheet(self.controller_button_style_stopped)
            self.controller_status.setText('‚èπÔ∏è Kontroler zatrzymany')
            self.status_label.setText("üîç Gotowy do uruchomienia")
            print("‚èπÔ∏è Kontroler zatrzymany")
            
    def check_auto_start_status(self):
        """Sprawdza aktualny status auto-startu"""
        try:
            import winreg
            key_path = r"Software\Microsoft\Windows\CurrentVersion\Run"
            app_name = "DEJE_Controller"
            
            key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, key_path, 0, winreg.KEY_READ)
            try:
                winreg.QueryValueEx(key, app_name)
                self.auto_start_enabled = True
                self.auto_start_btn.setText('üîÅ Auto-start: W≈ÅƒÑCZONY')
                self.status_label.setText("‚úÖ Auto-start w≈ÇƒÖczony")
            except FileNotFoundError:
                self.auto_start_enabled = False
                self.auto_start_btn.setText('üîÅ Auto-start: WY≈ÅƒÑCZONY')
                self.status_label.setText("üîç Gotowy do uruchomienia")
            winreg.CloseKey(key)
        except Exception as e:
            print(f"‚ùå B≈ÇƒÖd sprawdzania auto-startu: {e}")
            self.auto_start_enabled = False
            self.auto_start_btn.setText('üîÅ Auto-start: WY≈ÅƒÑCZONY')
            
    def toggle_auto_start(self):
        """Prze≈ÇƒÖcza auto-start"""
        if self.auto_start_enabled:
            # Wy≈ÇƒÖcz auto-start
            self.remove_startup()
        else:
            # W≈ÇƒÖcz auto-start
            self.setup_startup()
            
        # Od≈õwie≈º status
        self.check_auto_start_status()
            
    def setup_startup(self):
        """Ustawia automatyczne uruchamianie przy starcie systemu"""
        try:
            import winreg
            key_path = r"Software\Microsoft\Windows\CurrentVersion\Run"
            app_name = "DEJE_Controller"
            
            # Pe≈Çna ≈õcie≈ºka do aplikacji
            script_dir = os.path.dirname(os.path.abspath(__file__))
            script_path = os.path.join(script_dir, 'menu.py')
            # Uruchamiaj z flagƒÖ --background, aby od razu ukryƒá do zasobnika
            app_path = f'"{sys.executable}" "{script_path}" --background'
            
            # Otw√≥rz klucz rejestru i ustaw warto≈õƒá
            key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, key_path, 0, winreg.KEY_SET_VALUE)
            winreg.SetValueEx(key, app_name, 0, winreg.REG_SZ, app_path)
            winreg.CloseKey(key)
            print("‚úÖ Auto-start ustawiony z flagƒÖ --background")
            self.show_info_message("‚úÖ Sukces", "Auto-start zosta≈Ç w≈ÇƒÖczony!\n\nAplikacja bƒôdzie uruchamiaƒá siƒô automatycznie przy starcie systemu i ukrywaƒá siƒô do zasobnika.")
        except Exception as e:
            print(f"‚ùå B≈ÇƒÖd ustawiania auto-startu: {e}")
            self.show_error_message("‚ùå B≈ÇƒÖd", f"Nie mo≈ºna ustawiƒá auto-startu:\n{str(e)}")
            
    def remove_startup(self):
        """Usuwa automatyczne uruchamianie"""
        try:
            import winreg
            key_path = r"Software\Microsoft\Windows\CurrentVersion\Run"
            app_name = "DEJE_Controller"
            
            # Spr√≥buj usunƒÖƒá wpis
            try:
                key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, key_path, 0, winreg.KEY_SET_VALUE)
                winreg.DeleteValue(key, app_name)
                winreg.CloseKey(key)
                print("‚úÖ Auto-start usuniƒôty")
                self.show_info_message("‚úÖ Sukces", "Auto-start zosta≈Ç wy≈ÇƒÖczony!")
            except FileNotFoundError:
                print("‚ÑπÔ∏è Auto-start ju≈º by≈Ç usuniƒôty")
        except Exception as e:
            print(f"‚ùå B≈ÇƒÖd usuwania auto-startu: {e}")
            
